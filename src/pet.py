class PetWindow(QWidget): # AI-Generated by Claude
    def __init__(self, project: str = None): # AI-Generated by Claude
        super().__init__() # AI-Generated by Claude
        # Initialize skin registry
        self._skin_registry = SkinRegistry()
        self._current_skin_id = "default"
        # Initialize with default skin dimensions (will be updated in _load_config)
        default_skin = self._skin_registry.get_skin("default")
        self._renderer = PetRenderer(rows=default_skin.rows, cols=default_skin.cols) # AI-Generated by Claude
        self._animator = PetAnimator(self) # AI-Generated by Claude
        self._animator.set_skin(default_skin) # Apply initial skin
        self._drag_pos: QPoint | None = None # AI-Generated by Claude
        self._drag_moved = False # AI-Generated by Claude
        self._state_change_time = time.time()  # 记录状态改变的时间 # AI-Generated by Claude
        self._project_states: dict[str, dict] = {}  # hash → state data # AI-Generated by Claude
        self._effective_state: str = "idle" # AI-Generated by Claude
        self._effective_project: str | None = None # AI-Generated by Claude
        self._motion_type = "none" # AI-Generated by Claude
        self._motion_step = 0 # AI-Generated by Claude
        self._base_pos: QPoint | None = None # AI-Generated by Claude

        self._setup_window() # AI-Generated by Claude
        self._animator.frame_changed.connect(self.update) # AI-Generated by Claude
        self._animator.motion_changed.connect(self._on_motion_changed) # AI-Generated by Claude

        self._poll_timer = QTimer(self) # AI-Generated by Claude
        self._poll_timer.timeout.connect(self._poll_state) # AI-Generated by Claude
        self._poll_timer.start(500) # AI-Generated by Claude

        self._motion_timer = QTimer(self) # AI-Generated by Claude
        self._motion_timer.timeout.connect(self._apply_motion) # AI-Generated by Claude

        self._load_config() # AI-Generated by Claude
        if project: # AI-Generated by Claude
            self._setup_project_hooks(project) # AI-Generated by Claude
        self._cleanup_stale_files() # AI-Generated by Claude
        self._animator.start() # AI-Generated by Claude
        self._bubble = BubbleOverlay(self)  # AI-Generated by Claude

    # ── Window setup ─────────────────────────────────────────────────────────

    def _setup_window(self) -> None: # AI-Generated by Claude
        self.setWindowFlags( # AI-Generated by Claude
            Qt.WindowType.FramelessWindowHint # AI-Generated by Claude
            | Qt.WindowType.WindowStaysOnTopHint # AI-Generated by Claude
        )  # macOS 上不使用 Tool，直接用普通窗口 + 置顶
        self.setAttribute(Qt.WidgetAttribute.WA_TranslucentBackground) # AI-Generated by Claude
        self.setFixedSize(self._renderer.widget_width(), self._renderer.widget_height()) # AI-Generated by Claude
        screen = QApplication.primaryScreen().geometry() # AI-Generated by Claude
        x = screen.width() - self._renderer.widget_width() - 20 # AI-Generated by Claude
        # macOS: 右上角，留出菜单栏 + 气泡空间；Windows/Linux: 右下角
        if platform.system() == "Darwin":
            y = 50 + 25  # 菜单栏约 25px + 气泡空间 50px
        else:
            y = screen.height() - self._renderer.widget_height() - 60 # AI-Generated by Claude
        self.move(x, y) # AI-Generated by Claude
        self._base_pos = QPoint(x, y) # AI-Generated by Claude

    # ── Skin management ────────────────────────────────────────────────────────

    def _apply_skin(self, skin_id: str) -> None:
        """Apply a skin and update window size."""
        skin = self._skin_registry.get_skin(skin_id)
        self._current_skin_id = skin_id
        self._renderer.set_dimensions(skin.rows, skin.cols)
        self._animator.set_skin(skin)
        # Update window size
        self.setFixedSize(self._renderer.widget_width(), self._renderer.widget_height())
        # Reposition window to stay on screen
        screen = QApplication.primaryScreen().geometry()
        current_pos = self.pos()
        x = min(current_pos.x(), screen.width() - self._renderer.widget_width() - 20)
        y = min(current_pos.y(), screen.height() - self._renderer.widget_height() - 60)
        self.move(x, y)
        self._base_pos = QPoint(x, y)

    # ── Config / project ─────────────────────────────────────────────────────

    def _load_config(self) -> None: # AI-Generated by Claude
        if CONFIG_FILE.exists(): # AI-Generated by Claude
            try: # AI-Generated by Claude
                cfg = json.loads(CONFIG_FILE.read_text(encoding="utf-8")) # AI-Generated by Claude
                skin_id = cfg.get("skin", "default") # AI-Generated by Claude
                if skin_id != "default": # AI-Generated by Claude
                    self._apply_skin(skin_id) # AI-Generated by Claude
            except Exception: # AI-Generated by Claude
                pass # AI-Generated by Claude
        self._animator.set_state("idle") # AI-Generated by Claude

    def _save_config(self) -> None: # AI-Generated by Claude
        """Save current configuration to file.""" # AI-Generated by Claude
        cfg = {"skin": self._current_skin_id} # AI-Generated by Claude
        CONFIG_FILE.parent.mkdir(parents=True, exist_ok=True) # AI-Generated by Claude
        CONFIG_FILE.write_text( # AI-Generated by Claude
            json.dumps(cfg, ensure_ascii=False), encoding="utf-8" # AI-Generated by Claude
        ) # AI-Generated by Claude

    def _cleanup_stale_files(self) -> None: # AI-Generated by Claude
        """启动时清理超过 24 小时的状态文件，防止无限增长。""" # AI-Generated by Claude
        if not STATES_DIR.exists(): # AI-Generated by Claude
            return # AI-Generated by Claude
        cutoff = _dt.datetime.now(_dt.timezone.utc) - _dt.timedelta(hours=CLEANUP_MAX_AGE_HOURS) # AI-Generated by Claude
        for f in STATES_DIR.glob("*.json"): # AI-Generated by Claude
            try: # AI-Generated by Claude
                data = json.loads(f.read_text(encoding="utf-8")) # AI-Generated by Claude
                ts = _dt.datetime.fromisoformat(data.get("timestamp", "")) # AI-Generated by Claude
                if ts < cutoff: # AI-Generated by Claude
                    f.unlink() # AI-Generated by Claude
            except Exception: # AI-Generated by Claude
                try: # AI-Generated by Claude
                    f.unlink() # AI-Generated by Claude
                except Exception: # AI-Generated by Claude
                    pass # AI-Generated by Claude

    def _setup_project_hooks(self, project_dir: str) -> None: # AI-Generated by Claude
        """为指定项目注入 hooks（不存储到配置）。""" # AI-Generated by Claude
        from setup_hooks import inject_hooks # AI-Generated by Claude
        scripts_dir = os.path.abspath(os.path.join(_HERE, "..", "scripts")) # AI-Generated by Claude
        inject_hooks(project_dir, scripts_dir=scripts_dir) # AI-Generated by Claude

    # ── Painting ─────────────────────────────────────────────────────────────

    def paintEvent(self, event): # AI-Generated by Claude
        painter = QPainter(self) # AI-Generated by Claude
        painter.setRenderHint(QPainter.RenderHint.Antialiasing, False) # AI-Generated by Claude
